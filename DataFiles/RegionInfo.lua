function IsRegionKnown()
    return LOCATION_NUMBER > 1;
end

function IsRegionDeed(regionNumber)
    if (regionNumber >= _REGION_DEED_START and
        regionNumber <= _REGION_DEED_STOP) then
        return true;
    end
    return false;
end

function GetChatRegionNumber(regionName)
    for i=1, table.maxn(_CHAT_REGIONS) do
        local searchRegionName = GetRegionName(i);
        if (searchRegionName == regionName) then
            return i;
        end
    end
    return 0;
end

function GetCurrentRegionName()
    return GetRegionName(LOCATION_NUMBER);
end

function GetRegionName(regionNumber)
    if (regionNumber == -1) then
        return "offline";
    elseif (regionNumber == nil or 
        _CHAT_REGIONS[regionNumber] == nil or 
        _CHAT_REGIONS[regionNumber].CHAT_REGION == nil) then
        return "UNKNOWN";
    end
    return GetString(_CHAT_REGIONS[regionNumber].CHAT_REGION);
end

function AreWeInDeedRegion(deedLogPageNumber, deedLogPageTabNumber)
    return AreRegionsSame(LOCATION_NUMBER, deedLogPageNumber, deedLogPageTabNumber);
end

function AreWeInDeedAdjacentRegion(deedLogPageNumber, deedLogPageTabNumber)
    return AreRegionsAdjacent(LOCATION_NUMBER, deedLogPageNumber, deedLogPageTabNumber);
end

function AreRegionsSame(currentChatRegionNumber, deedLogPageNumber, deedLogPageTabNumber)
    if (not IsRegionKnown()) then
        -- We don't know where we are, don't worry about it.
        return false;
    end

    local region = _CHAT_REGIONS[currentChatRegionNumber];
    if (region.DEED_REGIONS == nil) then
        -- We're somewhere special, like an instance, festival, or Ettenmoors
        return false;
    end

    -- for each deed region this chat region represents:
    for iChatDeedRegion=1, table.maxn(_CHAT_REGIONS[currentChatRegionNumber].DEED_REGIONS) do
        local chatDeedRegion = _CHAT_REGIONS[currentChatRegionNumber].DEED_REGIONS[iChatDeedRegion];
        local i = chatDeedRegion["i"];
        local j = chatDeedRegion["j"];

        -- check if the chat-linked-deed-region is the deed-region
        if (i == deedLogPageNumber and j == deedLogPageTabNumber) then
            return true;
        end
    end

    return false;
end

function AreRegionsAdjacent(currentChatRegionNumber, deedLogPageNumber, deedLogPageTabNumber)
    if (not IsRegionKnown()) then
        -- We don't know where we are, don't worry about it.
        return false;
    end

    local region = _CHAT_REGIONS[currentChatRegionNumber];
    if (region.DEED_REGIONS == nil) then
        -- We're somewhere special, like an instance, festival, or Ettenmoors
        return false;
    end

    -- for each deed region this chat region represents:
    for iChatDeedRegion=1, table.maxn(_CHAT_REGIONS[currentChatRegionNumber].DEED_REGIONS) do
        -- check if the chat-linked-deed-region is adjacent to the deed-region
        local chatDeedRegion = _CHAT_REGIONS[currentChatRegionNumber].DEED_REGIONS[iChatDeedRegion];
        local i = chatDeedRegion["i"];
        local j = chatDeedRegion["j"];

        -- A region is adjacent to itself:
        if (i == deedLogPageNumber and j == deedLogPageTabNumber) then
            return true;
        else
            -- Check each region that we could have stepped into:
            for iAdjacentRegion=1, table.maxn(_ADJACENT_REGIONS[i][j]) do
                local adjacentRegion = _ADJACENT_REGIONS[i][j][iAdjacentRegion];
                local iAdjacentRegion = adjacentRegion["i"];
                local jAdjacentRegion = adjacentRegion["j"];
                if (iAdjacentRegion == deedLogPageNumber and 
                    jAdjacentRegion == deedLogPageTabNumber) then
                    return true;
                end
            end
        end
    end

    return false;
end

